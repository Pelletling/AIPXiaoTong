using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace AIPXiaoTong.Model.API
{
    public class BankCardBindingResponse : BaseResponse
    {
        private string _bankcardid { get; set; }
        private string _memberid { get; set; }


        //---------------------------------------------------------

        public string bankcardid { get { return !string.IsNullOrWhiteSpace(_bankcardid) ? _bankcardid : ""; } set { _bankcardid = value; } }
        public string memberid { get { return !string.IsNullOrWhiteSpace(_memberid) ? _memberid : ""; } set { _memberid = value; } }
    }
}
